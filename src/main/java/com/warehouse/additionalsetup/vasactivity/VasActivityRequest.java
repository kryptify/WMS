package com.warehouse.additionalsetup.vasactivity;

import com.warehouse.enums.OperationTypeEnum;

public class VasActivityRequest {

    private Long id;

    private String code;

    private String name;

    private Long warehouseId;

    private OperationTypeEnum operationType;

    private String createdFrom;

    private String createdTo;

    private String modifiedFrom;

    private String modifiedTo;

    private String searchFor;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCreatedFrom() {
        return createdFrom;
    }

    public void setCreatedFrom(String createdFrom) {
        this.createdFrom = createdFrom;
    }

    public String getCreatedTo() {
        return createdTo;
    }

    public void setCreatedTo(String createdTo) {
        this.createdTo = createdTo;
    }

    public String getModifiedFrom() {
        return modifiedFrom;
    }

    public void setModifiedFrom(String modifiedFrom) {
        this.modifiedFrom = modifiedFrom;
    }

    public String getModifiedTo() {
        return modifiedTo;
    }

    public void setModifiedTo(String modifiedTo) {
        this.modifiedTo = modifiedTo;
    }

    public String getSearchFor() {
        return searchFor;
    }

    public void setSearchFor(String searchFor) {
        this.searchFor = searchFor;
    }

    public Long getWarehouseId() {
        return warehouseId;
    }

    public void setWarehouseId(Long warehouseId) {
        this.warehouseId = warehouseId;
    }

    public OperationTypeEnum getOperationType() {
        return operationType;
    }

    public void setOperationType(OperationTypeEnum operationType) {
        this.operationType = operationType;
    }

    @Override
    public String toString() {
        return "VasActivityRequest [code=" + code + ", createdFrom=" + createdFrom + ", createdTo=" + createdTo
                + ", id=" + id + ", modifiedFrom=" + modifiedFrom + ", modifiedTo=" + modifiedTo + ", name=" + name
                + ", operationType=" + operationType + ", searchFor=" + searchFor + ", warehouseId=" + warehouseId
                + "]";
    }

}
